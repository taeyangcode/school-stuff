package com.jetbrains;

import java.awt.*;

public class Fonts {

    Font timesMainFont() {
        Font timesMainFont = new Font("Times", Font.PLAIN, 28);
        return timesMainFont;
    }

    Font timesSubFont() {
        Font timesSubFont = new Font("Times", Font.PLAIN, 22);
        return timesSubFont;
    }

    Font loseFont() {
        Font loseFont = new Font("Times", Font.PLAIN, 48);
        return loseFont;
    }

    Font winFont() {
        Font winFont = new Font("Times", Font.PLAIN, 48);
        return winFont;
    }

}
